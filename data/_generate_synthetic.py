# HIV: synthetic 30-year prevalence trajectory with two-period treatment rollout
# ============================================================================

def gen_hiv():
    np.random.seed(20260509)

    NM = NF = 0.5
    cMF = cFM = 1.5
    betaMF, betaFM = 0.20, 0.10
    tau_R = 10.0
    tau_R_treated = 0.5
    NU = 1  # normalized total population

    # Treatment coverage: zero before year 10, ramps to 60% by year 25
    def cov(t):
        if t < 10: return 0.0
        if t > 25: return 0.6
        return 0.6 * (t - 10) / 15.0

    def hiv_rhs(y, t):
        SM, IM, TM, SF, IF, TF = y
        c_eff = cov(t)
        # Effective infectious duration for untreated
        # Combined infectious load from group g: IM + TM*(tau_R_treated/tau_R) -- treated less infectious
        eff_M = IM + TM * (tau_R_treated/tau_R)
        eff_F = IF + TF * (tau_R_treated/tau_R)
        inc_M = betaMF * cMF * SM * eff_F / NF
        inc_F = betaFM * cFM * SF * eff_M / NM
        # Untreated -> treated at rate proportional to coverage
        rate_to_tx = c_eff / 2.0  # half-life 2 years to reach treatment when coverage active
        # Recovery (death from disease) at rate 1/tau_R for untreated
        return [-inc_M,
                inc_M - IM/tau_R - rate_to_tx*IM,
                rate_to_tx*IM - TM/tau_R_treated/2,  # treated stay alive longer
                -inc_F,
                inc_F - IF/tau_R - rate_to_tx*IF,
                rate_to_tx*IF - TF/tau_R_treated/2]

    t = np.linspace(0, 30, 361)  # monthly for 30 years
    y0 = [0.495, 0.005, 0.0, 0.495, 0.005, 0.0]
    sol = odeint(hiv_rhs, y0, t)
    SM, IM, TM, SF, IF, TF = sol.T

    prev_M = (IM + TM) / NM
    prev_F = (IF + TF) / NF
    overall = ((IM+TM) + (IF+TF)) / 1.0

    # Annual snapshots with multiplicative log-normal noise (surveillance estimates)
    annual_idx = np.arange(0, 361, 12)
    rows = []
    for i in annual_idx:
        year = float(t[i])
        # Multiplicative noise sigma=0.05 reflects survey-based prevalence uncertainty
        noise_M = np.exp(np.random.normal(0, 0.05))
        noise_F = np.exp(np.random.normal(0, 0.05))
        rows.append([round(year, 1),
                     round(float(prev_M[i] * noise_M), 5),
                     round(float(prev_F[i] * noise_F), 5),
                     round(float(cov(year)), 3)])

    _save_csv(HIV / "annual_prevalence_with_treatment.csv",
              ["year", "prevalence_M", "prevalence_F", "treatment_coverage"], rows)

    metadata = {
        "title": "Synthetic HIV bipartite prevalence, illustrative generalized epidemic with TasP rollout",
        "duration_years": 30,
        "generating_model": "bipartite (M-F) SI model with treatment-as-prevention compartment "
                           "and time-varying treatment coverage",
        "generating_parameters_TRUTH_FOR_NOTEBOOK_VERIFICATION": {
            "NM": NM, "NF": NF, "cMF": cMF, "cFM": cFM,
            "betaMF": betaMF, "betaFM": betaFM,
            "tau_R_untreated_years": tau_R,
            "tau_R_treated_years": tau_R_treated,
            "treatment_rollout_start_year": 10,
            "treatment_rollout_end_year": 25,
            "treatment_coverage_max": 0.6,
            "R0_untreated": 2.12,
        },
        "noise_model": "log-normal multiplicative noise with sigma=0.05 (survey-based prevalence estimates)",
        "seed": 20260509,
        "purpose": "Used by Chapter 17 notebooks to demonstrate HIV bipartite calibration, "
                   "treatment-as-prevention impact, and geographic-comparison structure.",
    }
    _save_json(HIV / "metadata.json", metadata)

    with open(HIV / "source_attribution.md", "w") as f:
        f.write(
            "# Synthetic HIV dataset\n\n"
            "**Type:** Synthetic, generated by `data/_generate_synthetic.py`.\n\n"
            "**License:** [CC BY-NC 4.0](../../LICENSE-CONTENT)\n\n"
            "**Purpose:** Pedagogical exemplar for the Chapter 17 case-study notebooks.\n\n"
            "**Seed:** 20260509\n\n"
            "**Real data alternatives:** UNAIDS AIDSinfo "
            "(https://aidsinfo.unaids.org); the DHS Program "
            "(https://dhsprogram.com) for survey-based prevalence.\n"
        )
    print(f"  wrote: data/hiv/  ({3} files)")


