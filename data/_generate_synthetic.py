"""
Generates synthetic exemplar datasets for the case-study notebooks.

Design principle: each dataset is the deterministic output of a documented
forward model with a documented seed. Notebooks pretend not to know the
generating parameters and try to recover them. This gives full
reproducibility (no external network dependencies) plus an honest
identifiability story (the truth is known, so estimator bias and
confidence intervals can be checked).

Run from /tmp/hyman2026essential/ (or any CWD):
    python data/_generate_synthetic.py
"""
import json
