from pathlib import Path

import numpy as np
from scipy.integrate import odeint

ROOT = Path(__file__).resolve().parent
COVID = ROOT / "covid"
DENGUE = ROOT / "dengue"
HIV = ROOT / "hiv"

# Make repo importable for notebook-style import paths
