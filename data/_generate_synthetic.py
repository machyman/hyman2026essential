# Dengue: synthetic 5-year monthly incidence with strong seasonality
# ============================================================================

def gen_dengue():
    np.random.seed(20260508)

    a = 0.4
    b = 0.4
    c = 0.4
    mu_v_baseline = 1.0/14.0
    mu_v_amp = 0.5
    gamma_h = 1.0/7.0
    xi_h = 1.0/(2.0*365.0)         # 2-year mean immunity (cross-serotype dynamics simplification)
    M_over_N = 5.0
    N_h = 1.0  # normalized

    def vh(y, t):
        SH, IH, RH, SV, IV = y
        # Phase pi -> mu_v MIN in middle of year (rainy season -> low vector mortality)
        mu_v = mu_v_baseline * (1 + mu_v_amp * np.cos(2*np.pi*t/365 + np.pi))
        Lam_h = a * b * M_over_N * IV
        Lam_v = a * c * IH
        return [-Lam_h*SH + xi_h*RH,
                Lam_h*SH - gamma_h*IH,
                gamma_h*IH - xi_h*RH,
                mu_v*(1-SV) - Lam_v*SV,
                Lam_v*SV - mu_v*IV]

    t = np.linspace(0, 365*5, 365*5+1)
    y0 = [0.95, 0.001, 0.049, 1.0, 0.0]
    sol = odeint(vh, y0, t)
    SH, IH, RH, SV, IV = sol.T
    new_inf = a * b * M_over_N * IV * SH
    new_inf = np.maximum(new_inf, 0.0)  # clip numerical noise

    # Aggregate to monthly counts (per 100K population)
    pop_per_100k = 100_000
    monthly_idx = np.arange(0, 365*5+1, 30)
    rows = []
    for i in range(len(monthly_idx)-1):
        lo, hi = monthly_idx[i], monthly_idx[i+1]
        cumulative = max(0.0, float(new_inf[lo:hi].sum() * pop_per_100k))
        observed = int(np.random.poisson(cumulative))
        rows.append([i+1, observed])

    _save_csv(DENGUE / "monthly_cases_per_100k.csv",
              ["month_index", "cases_per_100k"], rows)

    metadata = {
        "title": "Synthetic dengue monthly cases per 100K, illustrative tropical city",
        "duration_months": len(rows),
        "population_basis": pop_per_100k,
        "generating_model": "vector-host SIRS with seasonal vector mortality + 2-year waning immunity",
        "generating_parameters_TRUTH_FOR_NOTEBOOK_VERIFICATION": {
            "a": a, "b": b, "c": c,
            "mu_v_baseline": mu_v_baseline, "mu_v_amplitude": mu_v_amp,
            "gamma_h": gamma_h, "xi_h": xi_h, "M_over_N": M_over_N,
            "seasonal_phase_radians": "pi (mu_v minimum in mid-year, simulating wet season)",
        },
        "noise_model": "Poisson(monthly cumulative incidence per 100K)",
        "seed": 20260508,
        "purpose": "Used by Chapter 16 notebooks to demonstrate seasonal-R_0 calibration "
                   "and intervention-sensitivity analysis. Notebooks fit without knowing the truth.",
    }
    _save_json(DENGUE / "metadata.json", metadata)

    with open(DENGUE / "source_attribution.md", "w") as f:
        f.write(
            "# Synthetic dengue dataset\n\n"
            "**Type:** Synthetic, generated by `data/_generate_synthetic.py`.\n\n"
            "**License:** [CC BY-NC 4.0](../../LICENSE-CONTENT)\n\n"
            "**Purpose:** Pedagogical exemplar for the Chapter 16 case-study notebooks.\n\n"
            "**Seed:** 20260508\n\n"
            "**Real data alternatives:** WHO Dengue surveillance data; PAHO Dengue "
            "case data for the Americas.\n"
        )
    print(f"  wrote: data/dengue/  ({3} files)")


