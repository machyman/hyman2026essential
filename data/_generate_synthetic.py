# COVID-19: synthetic early-pandemic incidence (90 days, daily reported cases)
# ============================================================================

def gen_covid():
    np.random.seed(20260507)  # documented seed

    # Truth (notebooks pretend not to know these)
    c_I, beta = 14.0, 0.04
    tau_E, tau_R = 4.0, 5.0
    rho = 0.4  # reporting fraction
    N = 1.0e7
    I0 = 5_000.0
    E0 = 5_000.0
    S0 = N - E0 - I0

    def seir(y, t):
        S, E, I, R = y
        inc = c_I * beta * S * I / N
        return [-inc, inc - E/tau_E, E/tau_E - I/tau_R, I/tau_R]

    t = np.linspace(0, 90, 91)
    y0 = [S0, E0, I0, 0.0]
    sol = odeint(seir, y0, t)
    S, E, I, R = sol.T
    new_inf = -np.diff(np.concatenate([[S0], S]))   # daily new infections

    # Reporting model: Poisson with mean = rho * new_inf
    reported = np.random.poisson(rho * new_inf)

    # Save: dates as integer day index (notebooks add real dates if desired)
    rows = [[int(d), int(c)] for d, c in zip(t.astype(int), reported)]
    _save_csv(COVID / "metro_daily_cases.csv", ["day", "reported_cases"], rows)

    metadata = {
        "title": "Synthetic COVID-19 daily reported cases, illustrative metro area",
        "n_days": 91,
        "population_N": N,
        "generating_model": "SEIR with reporting fraction rho",
        "generating_parameters_TRUTH_FOR_NOTEBOOK_VERIFICATION": {
            "c_I": c_I, "beta": beta, "tau_E": tau_E, "tau_R": tau_R, "rho": rho,
            "S0": S0, "E0": E0, "I0": I0,
            "R0_basic": c_I * beta * tau_R,
        },
        "noise_model": "Poisson(rho * new_infections_per_day)",
        "seed": 20260507,
        "purpose": "Used by Chapter 15 notebooks to demonstrate early-pandemic "
                   "R_0 estimation, R_e(t) reconstruction, and under-reporting correction. "
                   "Notebooks fit without knowing the truth; the truth is here for verification only.",
    }
    _save_json(COVID / "metadata.json", metadata)

    with open(COVID / "source_attribution.md", "w") as f:
        f.write(
            "# Synthetic COVID-19 dataset\n\n"
            "**Type:** Synthetic, generated by `data/_generate_synthetic.py`.\n\n"
            "**License:** [CC BY-NC 4.0](../../LICENSE-CONTENT)\n\n"
            "**Purpose:** Pedagogical exemplar for the Chapter 15 case-study notebooks. "
            "Generating parameters are documented in `metadata.json` for verification "
            "purposes; notebooks treat the data as if unknown and attempt to recover them.\n\n"
            "**Seed:** 20260507\n\n"
            "**Real data alternatives:** For analysis with actual COVID-19 data, see "
            "Our World in Data (https://github.com/owid/covid-19-data) or the JHU CSSE "
            "COVID-19 Data Repository.\n"
        )
    print(f"  wrote: data/covid/  ({3} files)")


